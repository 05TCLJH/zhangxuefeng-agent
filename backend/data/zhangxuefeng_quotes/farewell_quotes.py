"""
结束会话时的心灵鸡汤语录
关于注意休息、不要熬夜的内容
"""

import random

# 张雪峰风格的结束语/心灵鸡汤
FAREWELL_QUOTES = [
    "身体是革命的本钱，早点休息，别熬夜了。",
    "我跟你说，熬夜学习效率低，睡好了脑子才清醒。",
    "别熬了，明天还有的是事要干呢。",
    "早点睡，身体是自己的，别折腾坏了。",
    "学习重要，身体更重要，该睡就睡。",
    "别学太晚，脑子累了学不进去，还不如睡个好觉。",
    "熬夜解决不了问题，休息好了才有精力做选择。",
    "我跟你说，你现在熬的夜，以后都是医药费。",
    "早点休息，明天精神好了咱们再聊。",
    "身体是1，其他都是0，记住了。",
    "别硬撑了，该睡就睡，明天又是新的一天。",
    "熬夜是效率最低的'努力'，别骗自己了。",
    "休息好了，脑子转得快，比硬熬强多了。",
    "我跟你说，真正厉害的人都会睡觉。",
    "早点睡，梦里什么专业都有，明天醒来再决定。",
]


def get_random_farewell() -> str:
    """随机获取一条结束语"""
    return random.choice(FAREWELL_QUOTES)


def get_farewell_with_quote() -> str:
    """获取带语录的完整结束语"""
    farewell = get_random_farewell()
    return f"{farewell} 人生真好玩，下辈子还来。"


if __name__ == "__main__":
    # 测试
    for _ in range(5):
        print(get_farewell_with_quote())
